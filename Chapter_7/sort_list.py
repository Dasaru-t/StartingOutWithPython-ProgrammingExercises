# The sort method rearranges the elements of a list so they appear in ascending order 
# (from the lowest value to the highest value).

my_list1 = [9, 1, 0, 2, 8, 6, 7, 4, 5, 3]
print('Original order:', my_list1)

my_list1.sort()
print('Sorted order:',my_list1)


my_list = ['beta', 'alpha', 'delta', 'gamma']
print('Original order:', my_list)

my_list.sort()
print('Sorted order:', my_list)